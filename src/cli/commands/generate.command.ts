/**
 * rhino generate
 *
 * Interactive wizard that asks:
 *   - What to create: model | policy | scope
 *   - Name (PascalCase)
 *   - Columns (for model only)
 *
 * Writes a stub file under the right directory.
 */
import * as path from 'path';
import {
  createInterface,
  ask,
  selectFromList,
  type ReadlineInterface,
} from '../utils/prompt';
import { writeFileSafely, fileExists } from '../utils/io';

export interface GenerateDeps {
  rl?: ReadlineInterface;
  cwd?: string;
}

export async function runGenerate(deps: GenerateDeps = {}): Promise<void> {
  const cwd = deps.cwd ?? process.cwd();
  const rl = deps.rl ?? createInterface();

  console.log('\n  Rhino :: Generate\n');

  const kind = await selectFromList(
    rl,
    'What would you like to generate?',
    ['model', 'policy', 'scope'],
    0,
  );

  const rawName = await ask(rl, `  Name (PascalCase, e.g. "BlogPost"): `);
  const name = toPascalCase(rawName);

  if (!name) {
    console.error('  Error: name is required.');
    rl.close();
    process.exit(1);
  }

  switch (kind) {
    case 'model':
      await generateModel(rl, cwd, name);
      break;
    case 'policy':
      generatePolicy(cwd, name);
      break;
    case 'scope':
      generateScope(cwd, name);
      break;
    default:
      console.error(`  Unknown type: ${kind}`);
      rl.close();
      process.exit(1);
  }

  rl.close();
}

// -----------------------------------------------------------------------
// Model
// -----------------------------------------------------------------------

async function generateModel(
  rl: ReadlineInterface,
  cwd: string,
  name: string,
): Promise<void> {
  const columnsRaw = await ask(
    rl,
    '  Columns (comma-separated, e.g. "title:string, body:text, published:boolean") []: ',
  );

  const columns = parseColumns(columnsRaw);
  const destPath = path.join(cwd, 'src', 'models', `${name}.model.ts`);

  if (fileExists(destPath)) {
    console.log(`  Skipped: ${destPath} already exists.`);
    return;
  }

  const content = renderModelStub(name, columns);
  writeFileSafely(destPath, content);
  console.log(`  Created: ${destPath}`);
}

function parseColumns(raw: string): Array<{ name: string; type: string }> {
  if (!raw.trim()) return [];
  return raw
    .split(',')
    .map((col) => col.trim())
    .filter(Boolean)
    .map((col) => {
      const [name, type = 'string'] = col.split(':').map((s) => s.trim());
      return { name: toCamelCase(name), type };
    });
}

function renderModelStub(
  name: string,
  columns: Array<{ name: string; type: string }>,
): string {
  const fieldLines = columns
    .map((c) => `  ${c.name}?: ${mapTsType(c.type)};`)
    .join('\n');

  return `/**
 * ${name} model stub — generated by \`npx rhino generate\`
 * Register in rhino.setup.ts:
 *   ${toCamelCase(name)}s: { model: '${name}' }
 */
import { RhinoModel } from '@rhino-dev/rhino-nestjs';

@RhinoModel()
export class ${name} {
  id!: number;
${fieldLines ? fieldLines + '\n' : ''}  createdAt?: Date;
  updatedAt?: Date;
}
`;
}


function renderPolicyStub(name: string): string {
  return `/**
 * ${name} policy stub — generated by \`npx rhino generate\`
 * Extend ResourcePolicy and override the methods you need.
 */
import { ResourcePolicy } from '@rhino-dev/rhino-nestjs';

export class ${name}Policy extends ResourcePolicy {
  // override index(user: any): boolean { ... }
  // override show(user: any, record: any): boolean { ... }
  // override store(user: any): boolean { ... }
  // override update(user: any, record: any): boolean { ... }
  // override destroy(user: any, record: any): boolean { ... }
}
`;
}


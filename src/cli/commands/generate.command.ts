function renderScopeStub(name: string): string {
  return `/**
 * ${name} scope stub — generated by \`npx rhino generate\`
 * Implement the \`apply\` method to filter / order the default query.
 */
import type { RhinoScope } from '@rhino-dev/rhino-nestjs';

export class ${name}Scope implements RhinoScope {
  apply(query: Record<string, any>, _context: any): Record<string, any> {
    // Example: return { ...query, where: { ...query.where, deletedAt: null } };
    return query;
  }
}
`;
}

// -----------------------------------------------------------------------
// String helpers
// -----------------------------------------------------------------------

function toPascalCase(str: string): string {
  if (!str) return '';
  return str
    .replace(/[-_\s]+(.)/g, (_, c: string) => c.toUpperCase())
    .replace(/^(.)/, (c: string) => c.toUpperCase());
}

function toCamelCase(str: string): string {
  const pascal = toPascalCase(str);
  return pascal.charAt(0).toLowerCase() + pascal.slice(1);
}

function mapTsType(prismaType: string): string {
  const map: Record<string, string> = {
    string: 'string',
    text: 'string',
    int: 'number',
    integer: 'number',
    float: 'number',
    decimal: 'number',
    boolean: 'boolean',
    bool: 'boolean',
    date: 'Date',
    datetime: 'Date',
    json: 'Record<string, unknown>',
  };
  return map[prismaType.toLowerCase()] ?? 'string';
}

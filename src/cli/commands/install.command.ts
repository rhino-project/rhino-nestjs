function generateSetupStub(
  multiTenant: boolean,
  orgIdentifierColumn: string,
): string {
  return `/**
 * Rhino setup — generated by \`npx rhino install\`
 * Adjust this file to match your application's model configuration.
 */
import { RhinoModule } from '@rhino-dev/rhino-nestjs';
// import { PrismaService } from './prisma/prisma.service';

export const rhinoConfig = {
  models: {
    // Register your models here, e.g.:
    // users: { model: 'User', policy: UserPolicy },
  },
  routeGroups: {
    default: {
      prefix: '',
      models: '*' as const,
    },
  },
  multiTenant: {
    enabled: ${multiTenant},
    organizationIdentifierColumn: '${orgIdentifierColumn}',
  },
  auth: {
    jwtSecret: process.env.JWT_SECRET ?? 'change-me',
    jwtExpiresIn: '7d',
    userModel: 'user',
    emailField: 'email',
    passwordField: 'password',
  },
};

// Add to your AppModule imports:
// RhinoModule.forRoot(rhinoConfig)
`;
}

async function installSkills(
  cwd: string,
  overrideSource?: string,
): Promise<void> {
  // The skills dir is shipped alongside the package under stubs/skills.
  // Resolve relative to this file so it works from both source and dist.
  const skillsSourceDir =
    overrideSource ??
    path.resolve(__dirname, '..', '..', '..', 'stubs', 'skills');

  const destDir = path.join(cwd, '.claude', 'commands');

  fs.mkdirSync(destDir, { recursive: true });

  if (!fileExists(skillsSourceDir)) {
    console.log(
      '  No skill files found (stubs/skills directory missing). Skipping.',
    );
    return;
  }

  const files = fs
    .readdirSync(skillsSourceDir)
    .filter((f) => f.startsWith('rhino-') && f.endsWith('.md'));

  if (files.length === 0) {
    console.log('  No rhino-*.md skill files found. Skipping.');
    return;
  }

  let installed = 0;
  for (const file of files) {
    const src = path.join(skillsSourceDir, file);
    const dest = path.join(destDir, file);
    fs.copyFileSync(src, dest);
    console.log(`  Installed: .claude/commands/${file}`);
    installed++;
  }

  console.log(`\n  ${installed} Rhino skills installed to .claude/commands/`);
  console.log(
    '  Run any skill in Claude Code: /rhino-feature, /rhino-review, etc.',
  );
}
